#!/usr/bin/env python3

# ||Imports||
# -------------------------------------------------------------------------------------------------------------------- #
import docker

# ||Variables||
# -------------------------------------------------------------------------------------------------------------------- #
socket = docker.from_env()

# ||Main||
# -------------------------------------------------------------------------------------------------------------------- #
if __name__ == "__main__":
    for container in socket.containers.list():
        stats = container.stats(decode = True).__next__()
        print(stats)
